// Problem: Print multiplication table of 4.
// This program prints the multiplication table of 3 using a simple for loop.
#include <iostream>
using namespace std;
int main()
{
    int n = 30;
    for (int i = 1; i <= n; i++)
    {
        cout << " 4 x " << i << " = " << 4 * i << endl;
    }
    return 0;
}