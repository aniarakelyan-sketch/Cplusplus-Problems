// Problem: Print the multiplication table of 7.
// This program prints the multiplication table of 7 using a simple for loop.
#include <iostream>
using namespace std;
int main()
{
    int n = 37;
    for (int i = 1; i <= n; i++)
    {
        cout << " 7 x " << i << " = " << 7 * i << endl;
    }
    return 0;
}