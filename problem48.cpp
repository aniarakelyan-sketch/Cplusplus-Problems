// Problem: Print the multiplication table of 10
#include <iostream>
using namespace std;

int main()
{
    // Loop through numbers from 1 to 10 and print the multiplication table of 10.
    for (int i = 1; i <= 10; i++)
    {
        cout << "10 x " << i << " = " << 10 * i << endl;
    }
    return 0;
}