// Problem: Read an integer from the user and print it.
#include <iostream>
using namespace std;

int main()
{
    int number;                                // Declare an int variable.
    cout << "Enter a number: ";                // Ask the user for input.
    cin >> number;                             // Read the number from the user.
    cout << "You entered: " << number << endl; // Print the number.
    return 0;
}
