// Problem: Print the multiplication tablo of 6.
// This program prints the multiplication table of 6 using simple for loop
#include <iostream>
using namespace std;
int main()
{
    int n = 15;
    for (int i = 1; i <= n; i++)
    {
        cout << " 6 x " << i << "=" << 6 * i << endl;
    }
    return 0;
}
