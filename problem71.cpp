// Problem: Input one character and print it on the screen.
// This program reads a single character from the user and prints it.
#include <iostream>
using namespace std;
int main()
{
    char ch; // Variable to store the character

    cout << "Enter a character: ";
    cin >> ch; // Input character from user

    cout << " You entered: " << ch << endl; // Print the character
    return 0;
}