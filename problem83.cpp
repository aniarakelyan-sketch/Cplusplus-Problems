// Problem: Print multiplication table of 8.
// This program prints the multiplication table of 8 using a simple for loop.
#include <iostream>
using namespace std;
int main()
{
    int n = 20;
    for (int i = 1; i <= n; i++)
    {
        cout << " 8 x " << i << " = " << 8 * i << endl;
    }

    return 0;
}